//Switch and break 
class q1 {
    public static void main(String[] args) {
        int num=2;
        switch (num){
        case 1:System.out.println("Monday");
        break;
        case 2:System.out.println("Tuesday");
        break;
        case 3:System.out.println("Wednesday");
        default:
        System.out.println("oho nooo");}
    }    
}